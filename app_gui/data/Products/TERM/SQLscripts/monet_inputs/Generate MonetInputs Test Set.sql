DROP TABLE IF EXISTS `MonetInputsTestSet`;
CREATE TABLE IF NOT EXISTS `MonetInputsTestSet` AS
SELECT
    First(`Monet Inputs Term`.PolNo) AS FirstOfPolNo,
    Min(`Monet Inputs Term`.PeriodIF) AS MinOfPeriodIF,
    `Monet Inputs Term`.Product,
    `Monet Inputs Term`.Joined,
    `Monet Inputs Term`.Sex1,
    `Monet Inputs Term`.Sex2,
    `Monet Inputs Term`.Country,
    Avg(`Monet Inputs Term`.AgeEnt1) AS AvgOfAgeEnt1,
    Min(`Monet Inputs Term`.AgeEnt2) AS MinOfAgeEnt2,
    `Monet Inputs Term`.Smoker1,
    `Monet Inputs Term`.Smoker2,
    `Monet Inputs Term`.PremFreq,
    Avg(`Monet Inputs Term`.SPWithOptions) AS AvgOfSPWithOptions,
    Avg(`Monet Inputs Term`.SPWithTI) AS AvgOfSPWithTI,
    Avg(`Monet Inputs Term`.SPadd) AS AvgOfSPadd,
    Avg(`Monet Inputs Term`.SumAssuredEnt) AS AvgOfSumAssuredEnt,
    Min(`Monet Inputs Term`.Term) AS MinOfTerm,
    `Monet Inputs Term`.BenefitType,
    Avg(`Monet Inputs Term`.MortgageRate) AS AvgOfMortgageRate,
    Avg(`Monet Inputs Term`.SumAssuredEnt2) AS AvgOfSumAssuredEnt2,
    `Monet Inputs Term`.BenefitType2,
    Avg(`Monet Inputs Term`.MortgageRate2) AS AvgOfMortgageRate2,
    `Monet Inputs Term`.Count,
    Avg(`Monet Inputs Term`.APWithOptions) AS AvgOfAPWithOptions,
    Avg(`Monet Inputs Term`.APWithTI) AS AvgOfAPWithTI,
    Avg(`Monet Inputs Term`.AnnualPremium) AS AvgOfAnnualPremium,
    `Monet Inputs Term`.CalcPremium,
    `Monet Inputs Term`.CalcEngine,
    `Monet Inputs Term`.ApplicationType,
    `Monet Inputs Term`.CommissionRP,
    `Monet Inputs Term`.CommissionSP,
    Min(`Monet Inputs Term`.TermComm) AS MinOfTermComm,
    `Monet Inputs Term`.AddPrem,
    `Monet Inputs Term`.RIModel,
    Avg(`Monet Inputs Term`.SumAssuredEnd) AS AvgOfSumAssuredEnd,
    999 AS Expr1,
    Min(`Monet Inputs Term`.Term) AS MinOfTerm1
FROM
    `Monet Inputs Term`
GROUP BY
    `Monet Inputs Term`.Product,
    `Monet Inputs Term`.Joined,
    `Monet Inputs Term`.Sex1,
    `Monet Inputs Term`.Sex2,
    `Monet Inputs Term`.Country,
    `Monet Inputs Term`.Smoker1,
    `Monet Inputs Term`.Smoker2,
    `Monet Inputs Term`.PremFreq,
    `Monet Inputs Term`.BenefitType,
    `Monet Inputs Term`.BenefitType2,
    `Monet Inputs Term`.Count,
    `Monet Inputs Term`.CalcPremium,
    `Monet Inputs Term`.CalcEngine,
    `Monet Inputs Term`.ApplicationType,
    `Monet Inputs Term`.CommissionRP,
    `Monet Inputs Term`.CommissionSP,
    `Monet Inputs Term`.AddPrem,
    `Monet Inputs Term`.RIModel;