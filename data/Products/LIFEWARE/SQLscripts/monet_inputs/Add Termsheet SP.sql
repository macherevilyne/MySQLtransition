INSERT INTO MonetTermsheet
SELECT
    TermsheetReport.`Tariff Symbol` AS tariffCode,
    'SP' AS TypeOfPrem,
    TermsheetReport.`IMC_SP%` AS IMCperc,
    TermsheetReport.IMCmin AS IMCmin,
    TermsheetReport.IMCmax AS IMCmax,
    TermsheetReport.IPC_SP AS IPC,
    IF(TermsheetReport.`AMC Base` = 'Premium', 0, `AMC%`) AS AMCperc,
    TermsheetReport.AMCmin AS AMCmin,
    TermsheetReport.AMCmax AS AMCmax,
    TermsheetReport.PF AS PF,
    IF(TermsheetReport.`AMC Base` = 'Premium', `AMC%`, 0) AS AMCpercP,
    GREATEST(TermsheetReport.`UFC_SPmax_amortized%`, TermsheetReport.`UFC_SPmax_direct%`) AS UFCperc,
    GREATEST(TermsheetReport.`UFC_SPmin_amortized%`, TermsheetReport.`UFC_SPmin_direct%`) AS UFC_minperc,
    TermsheetReport.DurMaxCommY AS DurMaxCommY,
    TermsheetReport.`RC%` AS RCperc,
    TermsheetReport.OPC AS OPCperc,
    0 AS RPCperc,
    TermsheetReport.DurPCY AS DurPCY,
    TermsheetReport.DurECY AS DurECY,
    0 AS TaxRatePrem,
    TermsheetReport.`CoCY%` AS CoCY,
    TermsheetReport.`DurSCY - SP` AS DurSCY,
    TermsheetReport.`Surrender charge initialRate` AS SVchargePerc,
    1 - TermsheetReport.`facdeath` AS facdeath,
    1.01 AS MinDeathBenefitFactor,
    TermsheetReport.RateAmmort AS RateAmmort,
    TermsheetReport.`Surrender fee` AS SurrenderFee,
    IF(`Tariff Symbol` LIKE 'wnb%', 0.007, 0) AS KickbackQLperc,
    0 AS KickbackBrokerPerc,
    0 AS IPCbroker,
    0 AS PFbroker,
    0 AS IMCpercBroker,
    IF(TermsheetReport.`AAC Base` = 'Premium', 0, `AAC%`) AS AMCpercBroker,
    IF(TermsheetReport.`AAC Base` = 'Premium', `AAC%`, 0) AS AMCpercPBroker,
    TermsheetReport.facdeath AS Rbperc,
    TermsheetReport.Tax AS Tax_effperc,
    0 AS DurMaxLB,
    0 AS SF_LBperc,
    0 AS LBperc,
    0 AS PerLoy
FROM TermsheetReport;